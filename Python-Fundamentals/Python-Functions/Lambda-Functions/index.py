# Lambda Functions

## Example: Add 10 to argument a, and return the result:
x = lambda a: a + 10
print(x(5))

# Lambda functions can take any number of arguments:
## Example: Multiply argument a with argument b and return the result:

x = lambda a, b: a * b
print(x(5, 6))

## Example: sum
x = lambda a, b, c: a + b + c
print(x(1, 2, 3))